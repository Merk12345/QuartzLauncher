const fs = require("fs");

const inputFile = "main.js";
const outputFile = "docs/MAIN_HANDLER_MAP.md";

const text = fs.readFileSync(inputFile, "utf8");
const lines = text.split(/\r?\n/);

const handleRegex = /ipcMain\.handle\(['"`]([^'"`]+)['"`]/;
const removeRegex = /ipcMain\.removeHandler\(['"`]([^'"`]+)['"`]/;
const functionRegex = /^(?:async\s+)?function\s+([a-zA-Z0-9_$]+)\s*\(/;

const handles = new Map();
const removes = new Map();
const functions = new Map();

function add(map, name, line) {
  if (!map.has(name)) map.set(name, []);
  map.get(name).push(line);
}

lines.forEach((line, index) => {
  const lineNo = index + 1;

  const handleMatch = line.match(handleRegex);
  if (handleMatch) add(handles, handleMatch[1], lineNo);

  const removeMatch = line.match(removeRegex);
  if (removeMatch) add(removes, removeMatch[1], lineNo);

  const functionMatch = line.match(functionRegex);
  if (functionMatch) add(functions, functionMatch[1], lineNo);
});

function tableFromMap(map, onlyDuplicates = false) {
  const rows = [];

  for (const [name, lineNumbers] of [...map.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
    if (onlyDuplicates && lineNumbers.length <= 1) continue;

    const finalLine = lineNumbers[lineNumbers.length - 1];
    rows.push(`| \`${name}\` | ${lineNumbers.length} | ${lineNumbers.join(", ")} | ${finalLine} |`);
  }

  return rows.length
    ? rows.join("\n")
    : "| None | 0 | - | - |";
}

const handlerCount = [...handles.values()].reduce((sum, lines) => sum + lines.length, 0);
const removeCount = [...removes.values()].reduce((sum, lines) => sum + lines.length, 0);
const functionCount = [...functions.values()].reduce((sum, lines) => sum + lines.length, 0);

const doc = `# Quartz main.js Handler Map

Generated from \`main.js\`.

Do not edit this file by hand. Regenerate it with:

\`\`\`bash
node tools/export-main-handler-map.js
\`\`\`

## Summary

| Item | Count |
| --- | ---: |
| main.js lines | ${lines.length} |
| ipcMain.handle calls | ${handlerCount} |
| ipcMain.removeHandler calls | ${removeCount} |
| named functions | ${functionCount} |

## Final effective IPC handlers

These are the final handler definitions Electron will use after all duplicate remove/re-handle blocks.

| Handler | Times defined | All handle lines | Final handle line |
| --- | ---: | --- | ---: |
${tableFromMap(handles)}

## Duplicate IPC handlers

These should be cleaned during the future main.js rewrite.

| Handler | Times defined | All handle lines | Final handle line |
| --- | ---: | --- | ---: |
${tableFromMap(handles, true)}

## Duplicate named functions

These should be reviewed before the rewrite because later definitions may override earlier ones.

| Function | Times defined | All definition lines | Final definition line |
| --- | ---: | --- | ---: |
${tableFromMap(functions, true)}

## removeHandler calls

These show where old handlers were being manually replaced.

| Handler | Times removed | All remove lines | Final remove line |
| --- | ---: | --- | ---: |
${tableFromMap(removes)}
`;

fs.writeFileSync(outputFile, doc);
console.log(`Wrote ${outputFile}`);
